let currentLang = localStorage.getItem('appLang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('appLang', lang);
    applyTranslations();
    // Re-render if navigate exists
    if (typeof navigate === 'function' && typeof currentPage !== 'undefined') {
        navigate(currentPage);
    }
}

function _t(key) {
    const dict = translations[currentLang] || translations["en"];
    return dict[key] || key;
}

function applyTranslations() {
    const dict = translations[currentLang] || translations["en"];

    // Recursively translate text nodes if their content exactly matches a key
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    let n;
    while (n = walker.nextNode()) {
        let originalText = n.nodeValue.trim();
        if (originalText && translations["en"][originalText]) {
            // Store original in data attribute if not already
            if (!n.parentElement.hasAttribute('data-i18n-key')) {
                n.parentElement.setAttribute('data-i18n-key', originalText);
            }
        }
    }

    // Now process elements with data-i18n-key
    document.querySelectorAll('[data-i18n-key]').forEach(el => {
        let key = el.getAttribute('data-i18n-key');
        if (dict[key]) {
            // Replace text node children carefully, keeping nested HTML tags intact if possible
            // Assuming simple text elements for now
            el.childNodes.forEach(child => {
                if (child.nodeType === Node.TEXT_NODE && child.nodeValue.trim() !== '') {
                    child.nodeValue = dict[key];
                }
            });
        }
    });

    // Update placeholders
    for (let key in dict) {
        document.querySelectorAll(`input[placeholder="${key}"], textarea[placeholder="${key}"]`).forEach(el => {
            el.setAttribute('data-i18n-placeholder', key);
        });
    }

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        let key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) {
            el.placeholder = dict[key];
        }
    });

    // Specifically update elements where JS dynamically set them
    if (typeof updatePageTranslations === 'function') {
        updatePageTranslations();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});
